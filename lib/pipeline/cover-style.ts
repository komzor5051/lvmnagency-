/**
 * Cover-art style for Влад Лямин's blog — cinematic editorial covers in the
 * a16z newsletter vein.
 *
 * The image is generated TEXTLESS with deliberate negative space on the left;
 * the title (serif, white) and byline (muted gold) are overlaid by the site,
 * never baked into the image. AI-rendered text — especially Cyrillic — is
 * unreliable, and a separate overlay keeps titles crisp and on-brand.
 *
 * Flow: an LLM turns the article idea into a one-sentence cinematic METAPHOR
 * (the `concept`), then `buildCoverPrompt(concept)` wraps it with the fixed
 * style directives below.
 */

export const COVER_STYLE_GUIDE = `
Кинематографичная редакторская обложка (как newsletter a16z / премиальные tech-издания).

ВИЗУАЛЬНЫЙ ЯЗЫК:
- Фотореализм: постановочная кинематографичная фотография либо фотореалистичный
  CG для сюрреалистичных концепций. НЕ иллюстрация, НЕ мем, НЕ мультяшность,
  НЕ «пластиковый» 3D-рендер.
- Свет: низкий ключ, драматичный, атмосферный. Один тёплый направленный источник
  (закат/рассвет, свет из окна в сумерках, лампа), часто за объектом — контровой
  свет, силуэт, дымка/туман/лёгкое смазывание движения.
- Цвет: приглушённый кинематографичный грейдинг — teal + amber, землистые тона,
  глубокие тени, тёплые блики, десатурация. Без неона и кислотных цветов.
- Настроение: спокойное, созерцательное, премиальное, чуть меланхоличное.
- Сюжет: ОДНА визуальная метафора идеи статьи — одинокая человеческая фигура в
  выразительной среде (офис в сумерках, стол у окна, человек на скале над городом)
  ИЛИ один сильный сюрреалистичный объект (парящая сфера с океаном внутри, башня
  из книг). Реалистично и со вкусом, не буквально, не клипарт.

КОМПОЗИЦИЯ (КРИТИЧНО):
- Главный объект — СПРАВА. Левая треть–половина кадра — спокойное, более тёмное
  пустое пространство под заголовок. Правило третей, выраженная глубина.

ЗАПРЕЩЕНО:
- Любой текст, буквы, слова, цифры, подписи, логотипы, водяные знаки, UI, графики.
- Мемность, гротеск, утрированные лица, коллаж, визуальный шум, кислотные цвета,
  фирменный лайм/бренд-цвета в кадре.
`;

/**
 * Build the image-generation prompt for a textless cinematic cover.
 * @param concept one-sentence visual metaphor of the article (in Russian).
 */
export function buildCoverPrompt(concept: string): string {
  return `Создай кинематографичную редакторскую обложку для статьи в блоге об AI и автоматизации бизнеса. Стиль — как обложки newsletter a16z: премиально, атмосферно, фотореалистично.

КОНЦЕПЦИЯ КАДРА (визуальная метафора темы): ${concept}

КОМПОЗИЦИЯ:
- Главный объект/герой — в ПРАВОЙ части кадра.
- Левая треть–половина кадра — спокойное, более тёмное негативное пространство (туда позже ляжет заголовок). НЕ размещай в левой части важные детали.
- Правило третей, выраженная глубина кадра, малая глубина резкости (мягкое боке).

СВЕТ И АТМОСФЕРА:
- Низкий ключ, кинематографичный свет. Один тёплый направленный источник (закат/рассвет, свет из окна в сумерках, настольная лампа), часто контровой — силуэты, ободковый свет.
- Лёгкая дымка/туман или мягкое смазывание движения. Плёночное зерно, эстетика 35мм.

ЦВЕТ:
- Приглушённый кинематографичный грейдинг: teal + amber, землистые тона, глубокие тени, тёплые блики, десатурация. Без неона, без кислотных и без ярких брендовых цветов.

ОБЯЗАТЕЛЬНО:
- Фотореализм. Одна сильная метафора, со вкусом, без буквальности.
- Горизонтальный формат 16:9.

СТРОГО ЗАПРЕЩЕНО:
- Любой текст, буквы, слова, цифры, подписи, логотипы, водяные знаки, UI, графики, инфографика.
- Мемность, мультяшность, гротескные/утрированные лица, коллаж, визуальный мусор, кислотные цвета.`;
}

