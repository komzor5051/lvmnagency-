export type ProductBuy =
  | { kind: "lava"; url: string } // lava.top checkout; empty url = not configured yet (fallback to Telegram)
  | { kind: "form"; url: string } // lead form, e.g. /audit
  | { kind: "waitlist" }; // email collection (course)

export type ProductType = "consultation" | "digital" | "service" | "coming-soon";

export type ProductFaq = { q: string; a: string };

export type Product = {
  id: string;
  type: ProductType;
  title: string;
  tagline: string; // short card description
  description: string[]; // paragraphs for /products
  price: number | null; // null for coming-soon
  priceLabel: string; // "10 000 ₽", "от 50 000 ₽", "скоро"
  meta: string; // "1:1 · 60 минут · онлайн"
  buy: ProductBuy;
  faq?: ProductFaq[];
  // CTA labels: action + what you get, never bare "купи".
  cta?: { buy: string; fallback: string }; // buy = checkout configured; fallback = manual Telegram channel
};

