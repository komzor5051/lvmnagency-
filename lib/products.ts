/** Product can be bought/ordered online right now (checkout or form is wired up). */
export function isBuyable(p: Product): boolean {
  return (
    (p.buy.kind === "lava" && p.buy.url.length > 0) ||
    (p.buy.kind === "form" && p.buy.url.length > 0)
  );
}

export function getProduct(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}
